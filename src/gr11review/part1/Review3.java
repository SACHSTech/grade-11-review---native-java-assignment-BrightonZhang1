package gr11review.part1;
import java.io.*;

public class Review3{
	public static void main(String[] args) throws IOException{
		// for loop incrementing and counting odd numbers from 20-100
		for(int i = 21; i < 100; i += 2){
		    System.out.println(i);
		}

		System.out.println();

		// for loop decrementing and counting backwards from 29-2
		for(int i = 29; i >= 2; i--){
		    System.out.println(i);
		}
	}
}
